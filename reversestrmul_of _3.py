# Boundary Condition(s): 3 <= Length of S <= 1000

# Input Format: The first line contains S

# The first line contains the alphabets as per the given condition.

# Output Format:

# Example input/Output 1:

# Input

# embezzling

# Output izm

# Explanation:

# After reversing the string embezzling, the string becomes gnilzzebme.

# In the string grilzzebme, the alphabets present at the positions of multiples of 3 are 1, z and m. 
# Hence the output is izm

